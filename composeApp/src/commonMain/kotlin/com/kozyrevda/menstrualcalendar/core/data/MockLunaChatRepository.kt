package com.kozyrevda.menstrualcalendar.core.data

import com.kozyrevda.menstrualcalendar.core.model.ChatMessage
import kotlinx.coroutines.delay
import kotlin.random.Random

/**
 * Mock-Луна: тёплые ответы по ключевым словам, работает офлайн.
 * Имитирует «печатает…» задержкой.
 */
class MockLunaChatRepository : LunaChatRepository {

    override suspend fun reply(history: List<ChatMessage>, cycleContext: String?): String {
        delay(Random.nextLong(700, 1400))
        val last = history.lastOrNull { it.role == ChatMessage.Role.User }
            ?.text?.lowercase().orEmpty()
        val ctx = cycleContext
            ?.let { " Сейчас $it — это тоже может влиять на состояние." }
            .orEmpty()

        fun lastHasAny(vararg keys: String) = keys.any { it in last }

        return when {
            lastHasAny("тревог", "страшно", "паник", "волну") ->
                "Слышу тебя — тревога очень выматывает, и то, что ты о ней говоришь, " +
                    "уже смелый шаг.$ctx Попробуй сейчас сделать медленный выдох длиннее вдоха. " +
                    "Расскажешь, в какие моменты тревога сильнее всего?"

            lastHasAny("пмс", "раздраж", "злюсь", "злость", "бесит") ->
                "Раздражительность перед месячными — это не «плохой характер», " +
                    "а физиология, ты не виновата.$ctx Что обычно помогает тебе выпустить пар — " +
                    "движение, тишина, что-то вкусное? Давай найдём твой способ."

            lastHasAny("сон", "сплю", "бессонниц", "не выспал") ->
                "Когда сон разлаживается, всё остальное тоже даётся тяжелее — " +
                    "неудивительно, что ты устала.$ctx Как проходит твой вечер перед сном? " +
                    "Иногда помогает за час до кровати убрать телефон и приглушить свет."

            lastHasAny("боль", "болит", "живот", "спина", "голова") ->
                "Мне жаль, что тебе больно. Тепло, покой и забота о себе " +
                    "сейчас важнее всех дел.$ctx Если боль сильная или необычная для тебя — " +
                    "пожалуйста, покажись врачу, я лишь рядом поддержать."

            lastHasAny("груст", "плак", "плохо", "одинок", "тоск") ->
                "Обнимаю тебя. Грусти можно давать место — она не делает тебя слабой.$ctx " +
                    "Хочешь рассказать, что случилось? Или просто побудем рядом — " +
                    "я никуда не тороплюсь."

            lastHasAny("спасибо", "благодар") ->
                "Всегда рядом 💗 Обращайся в любой момент — и не забывай отмечать " +
                    "чувства в дневнике, это правда помогает замечать себя."

            last.isBlank() ->
                "Привет! Это Луна. Как ты себя чувствуешь сегодня? " +
                    "Расскажи про настроение или самочувствие — я рядом."

            else -> listOf(
                "Понимаю тебя. Расскажи чуть подробнее — " +
                    "что ты чувствуешь в теле, когда это происходит?$ctx",
                "Спасибо, что делишься — это важно.$ctx Как давно ты это замечаешь? " +
                    "Попробуем вместе разобраться.",
                "Я рядом и слушаю. А что бы тебе сейчас хотелось — " +
                    "поддержки, совета или просто выговориться?$ctx",
            ).random()
        }
    }
}
