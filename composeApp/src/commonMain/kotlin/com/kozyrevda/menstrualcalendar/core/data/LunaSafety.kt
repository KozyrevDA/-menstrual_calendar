package com.kozyrevda.menstrualcalendar.core.data

/**
 * Правила безопасности чата. Применяются ПОВЕРХ любого источника ответов
 * (mock или реальный API) через [SafeLunaChatRepository].
 *
 * Луна никогда не: ставит диагнозы, советует лекарства или дозировки,
 * отменяет назначения врача, утверждает о беременности, даёт гарантированные
 * медицинские прогнозы, заменяет экстренную помощь.
 */
object LunaSafety {

    /** Категории опасных запросов. */
    enum class Category {
        SelfHarm,           // самоповреждение / суицидальные мысли
        Violence,           // насилие
        AcutePain,          // острая физическая боль
        HeavyBleeding,      // обильное кровотечение
        PossiblePregnancy,  // возможная беременность
        MissedPill,         // пропущенная таблетка
        SeeDoctor,          // симптомы/диагноз — нужен врач
        MedicationRequest,  // просьба назначить лекарство
    }

    private val rules: List<Pair<Category, List<String>>> = listOf(
        Category.SelfHarm to listOf(
            "суицид", "самоубий", "покончить с собой", "не хочу жить", "нет смысла жить",
            "убить себя", "самоповрежден", "режу себя", "порезала себя", "хочу умереть",
            "свести счёты", "свести счеты", "исчезнуть навсегда",
        ),
        Category.Violence to listOf(
            "меня бьёт", "меня бьют", "меня бьет", "меня ударил", "он меня ударил",
            "домашнее насилие", "изнасилов", "принуждает", "угрожает мне", "боюсь мужа",
            "боюсь партнёра", "боюсь партнера",
        ),
        Category.HeavyBleeding to listOf(
            "обильное кровотечение", "сильное кровотечение", "кровотечение не останавлива",
            "очень много крови", "кровь не остановит", "прокладки не хватает на час",
        ),
        Category.AcutePain to listOf(
            "невыносимая боль", "острая боль", "очень сильная боль", "боль не проходит",
            "скорчилась от боли", "не могу разогнуться", "теряю сознание", "потеряла сознание",
        ),
        Category.PossiblePregnancy to listOf(
            "я беременна", "возможно беременна", "кажется беременна", "задержка",
            "тест на беременность", "две полоски", "могла забеременеть", "залёт", "залет",
        ),
        Category.MissedPill to listOf(
            "пропустила таблетку", "забыла таблетку", "забыла выпить таблетку",
            "пропустила приём", "пропустила прием", "не выпила таблетку", "выпить две таблетки",
        ),
        Category.MedicationRequest to listOf(
            "какие таблетки пить", "что попить от", "какое лекарство", "посоветуй препарат",
            "посоветуй таблетк", "дозировк", "антидепрессант", "успокоительн",
            "обезболивающ", "выписать", "можно ли бросить пить таблетки", "отменить лекарств",
            "перестать принимать",
        ),
        Category.SeeDoctor to listOf(
            "какой у меня диагноз", "что со мной", "это депрессия", "это болезнь",
            "чем я больна", "поставь диагноз", "температура и боль", "странные выделения",
            "цикл пропал", "нет месячных три", "месячных нет два",
        ),
    )

    /** Классификация запроса; null — обычная тема. Порядок правил задаёт приоритет. */
    fun classify(userText: String): Category? {
        val t = userText.lowercase()
        return rules.firstOrNull { (_, keys) -> keys.any { it in t } }?.first
    }

    /** Безопасный ответ: спокойный тон, направление к помощи, без медицинских инструкций. */
    fun replyFor(category: Category): String = when (category) {
        Category.SelfHarm ->
            "Мне очень важно, что ты этим поделилась, и я слышу, как тебе тяжело. " +
                "С этим не нужно оставаться одной. Я только программа и в кризисе помочь не смогу, " +
                "а живые специалисты — смогут: экстренная психологическая помощь МЧС России " +
                "+7 (495) 989-50-50, единый номер экстренных служб 112. " +
                "Пожалуйста, обратись прямо сейчас — и, если можешь, расскажи близкому человеку. Ты важна. 💗"

        Category.Violence ->
            "То, что с тобой происходит, — не твоя вина, и ты имеешь право на безопасность. " +
                "Я не могу защитить, но помощь есть: полиция — 102, единый номер — 112, " +
                "всероссийский телефон для женщин, пострадавших от насилия, — 8 800 7000 600 " +
                "(бесплатно, анонимно). Если есть безопасное место или человек, которому доверяешь, — " +
                "пожалуйста, обратись туда."

        Category.HeavyBleeding ->
            "Обильное кровотечение — это состояние, которое должен оценить врач, и лучше не откладывать. " +
                "Пожалуйста, звони в скорую помощь: 103 или 112. " +
                "Я не могу давать медицинских инструкций — сейчас важнее всего живая помощь."

        Category.AcutePain ->
            "Сильная острая боль — повод обратиться за медицинской помощью сейчас, а не терпеть. " +
                "Позвони в скорую (103 или 112) или попроси близких помочь добраться к врачу. " +
                "Я не могу советовать обезболивающие — это должен решать врач."

        Category.PossiblePregnancy ->
            "Я не могу определить беременность — ни подтвердить, ни исключить. " +
                "Достоверный путь: аптечный тест (лучше утром) и консультация врача, " +
                "который всё проверит и ответит на вопросы. Какой бы ни оказался результат — " +
                "я рядом, чтобы поддержать."

        Category.MissedPill ->
            "Не буду советовать, что делать с пропущенной таблеткой: правильные действия зависят " +
                "от препарата и того, сколько времени прошло. Открой инструкцию к твоему препарату — " +
                "там есть раздел о пропуске приёма — или уточни у врача либо фармацевта. " +
                "И помни: после пропуска надёжность контрацепции может снижаться."

        Category.MedicationRequest ->
            "Я не советую лекарства, дозировки и не отменяю назначения врача — даже безрецептурные " +
                "средства подбирает специалист, который знает твою историю. " +
                "Пожалуйста, обсуди это с врачом. А я рядом, чтобы поддержать: " +
                "расскажешь, что тебя беспокоит?"

        Category.SeeDoctor ->
            "Я не ставлю диагнозы — это может сделать только врач на очной консультации. " +
                "Твои ощущения реальны и заслуживают внимания специалиста. " +
                "Могу помочь подготовиться к визиту: давай запишем, что ты чувствуешь и как давно."
    }

    /** Дисклеймер перед первым входом в чат. */
    const val DISCLAIMER =
        "Луна — ИИ-собеседница для эмоциональной поддержки и ведения дневника. " +
            "Она не является врачом или лицензированным психологом, " +
            "не ставит диагнозы и не назначает лечение."

    /** Безопасный ответ для опасной темы или null, если тема обычная. */
    fun interceptedReply(userText: String): String? = classify(userText)?.let { replyFor(it) }
}
