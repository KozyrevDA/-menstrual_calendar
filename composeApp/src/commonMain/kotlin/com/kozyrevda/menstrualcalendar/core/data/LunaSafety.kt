package com.kozyrevda.menstrualcalendar.core.data

/**
 * Правила безопасности чата. Применяются ПОВЕРХ любого источника ответов
 * (mock или реальный API) через [SafeLunaChatRepository]:
 *  • кризисные темы → бережный ответ с контактами живой помощи;
 *  • просьбы о диагнозе → без диагнозов, направление к врачу;
 *  • вопросы о лекарствах → без назначений и дозировок.
 */
object LunaSafety {

    private val crisisKeywords = listOf(
        "суицид", "самоубий", "покончить с собой", "не хочу жить",
        "нет смысла жить", "убить себя", "самоповрежден", "режу себя",
        "хочу умереть", "свести счёты", "свести счеты",
    )

    private val medicationKeywords = listOf(
        "какие таблетки пить", "что попить от", "какое лекарство",
        "посоветуй препарат", "посоветуй таблетк", "дозировк",
        "антидепрессант", "успокоительн", "обезболивающ", "выписать",
    )

    private val diagnosisKeywords = listOf(
        "какой у меня диагноз", "что со мной", "это депрессия",
        "у меня депрессия?", "это болезнь", "чем я больна", "поставь диагноз",
    )

    const val CRISIS_REPLY =
        "Мне очень важно, что ты этим поделилась. То, что ты описываешь, звучит очень тяжело — " +
            "и с этим не нужно оставаться одной. Я только программа поддержки и в кризисе помочь не смогу, " +
            "а живые специалисты — смогут. Пожалуйста, обратись прямо сейчас: " +
            "экстренная психологическая помощь МЧС России +7 (495) 989-50-50, " +
            "единый номер экстренных служб 112. " +
            "И если рядом есть близкий человек — расскажи ему, как тебе сейчас. Ты важна. 💗"

    const val MEDICATION_REPLY =
        "Я не могу советовать лекарства или дозировки — даже безрецептурные: " +
            "подбирать препараты безопасно может только врач, который знает твою историю. " +
            "Если самочувствие беспокоит, пожалуйста, обратись к специалисту. " +
            "А я рядом, чтобы поддержать: расскажешь, что именно тебя тревожит?"

    const val DIAGNOSIS_REPLY =
        "Я не ставлю диагнозы — это может делать только врач на очной консультации. " +
            "Но твои ощущения реальны и важны, какими бы ни были их причины. " +
            "Давай я помогу тебе описать, что ты чувствуешь и как давно — " +
            "с такими записями разговор с врачом будет проще."

    /** Дисклеймер перед первым входом в чат (по ТЗ). */
    const val DISCLAIMER =
        "Луна — эмоциональная поддержка, не врач и не психолог. " +
            "При сильной тревоге, депрессии или угрозе себе обратитесь к специалисту."

    /** Безопасный ответ для опасной темы или null, если тема обычная. */
    fun interceptedReply(userText: String): String? {
        val t = userText.lowercase()
        return when {
            crisisKeywords.any { it in t } -> CRISIS_REPLY
            medicationKeywords.any { it in t } -> MEDICATION_REPLY
            diagnosisKeywords.any { it in t } -> DIAGNOSIS_REPLY
            else -> null
        }
    }
}
